void draw_char(int ch){
	int cpuid;
	const u8 * map;
	int dx,dy;
	int color;
	int pos;
	int i;
	int tmp;
	int chr_width = 8;
	
	if(ch == '\n'){
		x = 0;
		y += 16;
	}
	else if(ch == '\t'){
		x += 0x3f;
		x &= 0xffffc0;
	}
	else if(ch == '\r'){
		x = 0;
		return;
	}
	if(x + 8 > width){
		x = 0;
		y += 16;
	}
	if(y >= high){
		memcpy(lfb,lfb + 16 * byte_per_scan_line,(high - 16) * byte_per_scan_line);
		memset(lfb + (high - 16) * byte_per_scan_line,0xff,16 * byte_per_scan_line);
		y -= 16;
	}
	if(ch == '\n' || ch == '\t') {
		return;
	}
	if(!font){
		if(ch <= 0x1f || ch >= 0x7f) map = tofu;
		else map = _bmp + 16 * (ch - 0x20);
	}
	else{
		tmp = font[ch];
		if(!tmp) {
			map = tofu;
		}
		else{
			chr_width = tmp >> 24;
			map = (void*)(((u64)font) + (tmp & 0x00ffffff));
		}
	}
	for(dy = 0;dy < 16;dy++){
		for(dx = 0;dx < chr_width;dx++){
			if(bt(map,dy * chr_width + dx)) color = text_color;
			else color = back_color;
			pos = (x + dx) * byte_per_pixel + (y + dy) * byte_per_scan_line;
			if(byte_per_pixel == 1)
				lfb[pos] = color;
			else if(byte_per_pixel == 2)
				*(u16*)(lfb + pos) = color;
			else if(byte_per_pixel == 4)
				*(u32*)(lfb + pos) = color;
			else{
				lfb[pos] = color;
				lfb[pos + 1] = color >> 8;
				lfb[pos + 2] = color >> 16;
			}
		}
	}
	x += chr_width;
}
